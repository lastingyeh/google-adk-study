from google.adk.agents.run_config import RunConfig, StreamingMode
from google.adk.runners import Runner
from google.adk.sessions import InMemorySessionService
from google.genai import types

from rag.agent import root_agent


def test_agent_stream() -> None:
    """
    Agent 串流功能整合測試。
    測試 Agent 是否回傳有效的串流回應。

    重點說明:
    1. 使用 InMemorySessionService 建立測試會話
    2. 初始化 Runner 並掛載 root_agent
    3. 發送測試訊息 "Why is the sky blue?"
    4. 驗證回傳的事件列表不為空且包含文字內容
    """

    session_service = InMemorySessionService()

    session = session_service.create_session_sync(user_id="test_user", app_name="test")
    runner = Runner(agent=root_agent, session_service=session_service, app_name="test")

    message = types.Content(
        role="user", parts=[types.Part.from_text(text="Why is the sky blue?")]
    )

    events = list(
        runner.run(
            new_message=message,
            user_id="test_user",
            session_id=session.id,
            run_config=RunConfig(streaming_mode=StreamingMode.SSE),
        )
    )
    assert len(events) > 0, "預期至少收到一條訊息"

    has_text_content = False
    for event in events:
        if (
            event.content
            and event.content.parts
            and any(part.text for part in event.content.parts)
        ):
            has_text_content = True
            break
    assert has_text_content, "預期至少收到一條包含文字內容的訊息"
